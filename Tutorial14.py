# Python - Modify Strings

## Upper Case

c = 'Developer'
print(c.upper())

## Lower Case

r = 'DEVELOPER'
print(r.lower())

## Remove Whitespace

s = 'Developer'
print(s.strip())

## Replace String

t = 'Web Developer'
print(t.replace('Web','Python'))

## Split String

a = 'Python Devloper'
print(a.split())
