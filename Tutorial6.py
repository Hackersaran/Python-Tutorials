# Python Variables - Assign Multiple Values

## Many Values to Multiple Variables

s, a, r = "Full", "Stack", "Web Developer"
print(s)
print(a)
print(r)

## One Value to Multiple Variables

x=y=z='Full Stack Web Developer'
print(x)
print(y)
print(z)

## Unpack a Collection

Cars = ['Audi','Aston Martin','Mini Cooper','Benz']
w,x,y,z=Cars
print(w)
print(x)
print(y)
print(z)
