# Python Variable

## Creating Variables

x = 'SARAN'             # x is a str
y = 12062008            # y is a int
print(x,y)

## Casting

x = str(3)              # x is '3'
y = int(3)              # y is 3
z = float(3)            # z is 3.0
print(x,y,z)

