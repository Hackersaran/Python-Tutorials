## Escape Characters



# \'	    Single Quote	
# \\	    Backslash	
# \n	    New Line	
# \r	    Carriage Return	
# \t	    Tab	
# \b	    Backspace	
# \f	    Form Feed	
# \ooo	Octal value	
# \xhh	Hex value



a = 'It\'s alright.'
print(a)
b = 'Let me insert \\ SARAN'
print(b)
c = 'I am the best\ndeveloper in \nthe world'
print(c)
d = 'I am the \rbest developer in the world'
print(d)
e = 'I am the best\tdeveloper in the world'
print(e)
f = 'I\b am the best developer in the world'
print(f)
g = 'I am the best developer in the world \f'
print(g)
