# Python - Variable Names

## Legal variable names:

myvar = "John"
my_var = "John"
_my_var = "John"
myVar = "John"
MYVAR = "John"
myvar2 = "John"

## Illegal variable names:

'''
2myvar = "John"
my-var = "John"
my var = "John"
'''

## Multi Words Variable Names

srevsbttwtvwetwetvw = 'Saran'

## Camel Case

myVariableName = "Saran"

## Pascal Case

MyVariableName = "Saran"

## Snake Case

my_variable_name = "Saran"
